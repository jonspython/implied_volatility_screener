def get_option_statistics(ticker, current_price):

    today = datetime.today()

    expirations = []

    for exp in ticker.options:

        dte = (datetime.strptime(exp,"%Y-%m-%d")-today).days

        if dte > 0:
            expirations.append((exp,dte))

    if len(expirations)==0:
        return {}

    expiration,dte = min(
        expirations,
        key=lambda x: abs(x[1]-12)
    )

    puts = ticker.option_chain(expiration).puts.copy()

    # First OTM put
    otm = puts[puts["strike"] <= current_price]

    if len(otm):

        strike = otm["strike"].max()
        option = otm.loc[
            otm["strike"]==strike
        ].iloc[0]

    else:

        puts["distance"] = abs(
            puts["strike"]-current_price
        )

        option = puts.loc[
            puts["distance"].idxmin()
        ]

        strike = option["strike"]

    bid = option["bid"]
    ask = option["ask"]

    midpoint = (bid+ask)/2

    collateral = strike-midpoint

    annualized_yield = np.nan

    if collateral>0:

        annualized_yield = (
            midpoint /
            collateral *
            365/dte
        )

    spread_pct = np.nan

    if midpoint>0:

        spread_pct = (
            ask-bid
        )/midpoint

    premium_pct = midpoint/strike

    otm_pct = (
        current_price-strike
    )/current_price

    return {

        "Expiration":expiration,
        "DTE":dte,

        "Strike":strike,

        "Bid":bid,
        "Ask":ask,
        "Midpoint":midpoint,

        "Implied Volatility":
            option["impliedVolatility"],

        "Premium %":
            premium_pct,

        "OTM %":
            otm_pct,

        "Annualized Yield":
            annualized_yield,

        "Bid/Ask Spread %":
            spread_pct,

        "Option Volume":
            option["volume"],

        "Open Interest":
            option["openInterest"]

    }


